-- Script de inicialización para PostgreSQL
-- Proyecto: Issue Tracker

-- 1. Eliminación de tablas si existen (en orden inverso a la creación)
DROP TABLE IF EXISTS comment;
DROP TABLE IF EXISTS issue;
DROP TABLE IF EXISTS project_member;
DROP TABLE IF EXISTS project;
DROP TABLE IF EXISTS app_user_roles;
DROP TABLE IF EXISTS app_user;
DROP TABLE IF EXISTS app_role;

-- 2. Creación de tablas

-- Tabla de Roles
CREATE TABLE app_role (
    role_id BIGINT PRIMARY KEY,
    name VARCHAR(10) NOT NULL UNIQUE,
    description VARCHAR(100) NOT NULL
);

-- Tabla de Usuarios
CREATE TABLE app_user (
    user_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email VARCHAR(255) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    first_name VARCHAR(255) NOT NULL,
    last_name VARCHAR(255) NOT NULL
);

-- Tabla intermedia Usuarios-Roles
CREATE TABLE app_user_roles (
    user_id BIGINT NOT NULL,
    role_id BIGINT NOT NULL,
    PRIMARY KEY (user_id, role_id),
    CONSTRAINT fk_user FOREIGN KEY (user_id) REFERENCES app_user(user_id),
    CONSTRAINT fk_role FOREIGN KEY (role_id) REFERENCES app_role(role_id)
);

-- Tabla de Proyectos
CREATE TABLE project (
    project_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    description TEXT NOT NULL
);

-- Tabla intermedia Proyectos-Miembros
CREATE TABLE project_member (
    project_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL,
    PRIMARY KEY (project_id, user_id),
    CONSTRAINT fk_project FOREIGN KEY (project_id) REFERENCES project(project_id),
    CONSTRAINT fk_member_user FOREIGN KEY (user_id) REFERENCES app_user(user_id)
);

-- Tabla de Incidencias (Issues)
CREATE TABLE issue (
    issue_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(200) NOT NULL,
    description TEXT NOT NULL,
    status VARCHAR(20) NOT NULL DEFAULT 'OPEN',
    created_at TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    project_id BIGINT NOT NULL,
    reporter_id BIGINT NOT NULL,
    assignee_id BIGINT,
    CONSTRAINT fk_issue_project FOREIGN KEY (project_id) REFERENCES project(project_id),
    CONSTRAINT fk_issue_reporter FOREIGN KEY (reporter_id) REFERENCES app_user(user_id),
    CONSTRAINT fk_issue_assignee FOREIGN KEY (assignee_id) REFERENCES app_user(user_id)
);

-- Tabla de Comentarios
CREATE TABLE comment (
    comment_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    content TEXT NOT NULL,
    created_at TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    issue_id BIGINT NOT NULL,
    author_id BIGINT NOT NULL,
    CONSTRAINT fk_comment_issue FOREIGN KEY (issue_id) REFERENCES issue(issue_id),
    CONSTRAINT fk_comment_author FOREIGN KEY (author_id) REFERENCES app_user(user_id)
);

